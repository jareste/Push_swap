#include "../inc/push_swap.h"

void	array3(int *a, t_list *l)
{
	if (l->len == 2)
	{
		if (a[0] > a[1])
			sa(l->sa);
	}
	else if (a[0] < a[1] && a[0] < a[2] && a[1] > a[2])
	{
		rrr(l, 'a');
		sa(l->sa);
	}
	else if (a[0] > a[1] && a[0] < a[2] && a[1] < a[2])
		sa(l->sa);
	else if (a[0] < a[1] && a[0] > a[2] && a[1] > a[2])
		rrr(l, 'a');
	else if (a[1] < a[2] && a[0] > a[1] && a[0] > a[2])
		rr(l, 'a');
	else if (a[1] > a[2] && a[0] > a[1] && a[0] > a[2])
	{
		rr(l, 'a');
		sa(l->sa);
	}
}
