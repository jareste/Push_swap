#include "push_swap.h"

void	array3(int *a, int *b, int len)
{
	if (len == 2)
	{
		if (a[0] > a[1])
			sa(a);
	}
	else if (a[0] < a[1] && a[0] < a[2] && a[1] > a[2])
	{
		rrr(a, b, len, 'a');
		sa(a);
	}
	else if (a[0] > a[1] && a[0] < a[2] && a[1] < a[2])
		sa(a);
	else if (a[0] < a[1] && a[0] > a[2] && a[1] > a[2])
		rrr(a, b, len, 'a');
	else if (a[1] < a[2] && a[0] > a[1] && a[0] > a[2])
		rr(a, b, len, 'a');
	else if (a[1] > a[2] && a[0] > a[1] && a[0] > a[2])
	{
		rr(a, b, len, 'a');
		sa(a);
	}
}
